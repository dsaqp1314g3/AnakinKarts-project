package edu.upc.eetac.dsa.dsaqp1314g3.AnakinKarts.api.model;

public class User {
	
	String name=null;
	String username=null;
	String userpass=null;
	String email=null;
	int nphone=0;
	String ciudad=null;
	String cp=null;
	String calle=null;
	int numportal=0;
	int piso=0;
	int numpuerta=0;
	
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getUserpass() {
		return userpass;
	}
	public void setUserpass(String userpass) {
		this.userpass = userpass;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public int getNphone() {
		return nphone;
	}
	public void setNphone(int nphone) {
		this.nphone = nphone;
	}
	public String getCiudad() {
		return ciudad;
	}
	public void setCiudad(String ciudad) {
		this.ciudad = ciudad;
	}
	public String getCp() {
		return cp;
	}
	public void setCp(String cp) {
		this.cp = cp;
	}
	public String getCalle() {
		return calle;
	}
	public void setCalle(String calle) {
		this.calle = calle;
	}
	public int getNumportal() {
		return numportal;
	}
	public void setNumportal(int numportal) {
		this.numportal = numportal;
	}
	public int getPiso() {
		return piso;
	}
	public void setPiso(int piso) {
		this.piso = piso;
	}
	public int getNumpuerta() {
		return numpuerta;
	}
	public void setNumpuerta(int numpuerta) {
		this.numpuerta = numpuerta;
	}
	
	
}
